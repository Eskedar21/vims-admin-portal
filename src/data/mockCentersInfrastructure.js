export function getCenterJurisdictionPath(center) {
  const parts = [];
  if (center.region_id) {
    const region = mockAdminUnits.find(u => u.admin_unit_id === center.region_id);
    if (region) parts.push(region.admin_unit_name_en);
  }
  if (center.zone_id) {
    const zone = mockAdminUnits.find(u => u.admin_unit_id === center.zone_id);
    if (zone) parts.push(zone.admin_unit_name_en);
  }
  if (center.sub_city_id) {
    const subCity = mockAdminUnits.find(u => u.admin_unit_id === center.sub_city_id);
    if (subCity) parts.push(subCity.admin_unit_name_en);
  }
  if (center.woreda_id) {
    const woreda = mockAdminUnits.find(u => u.admin_unit_id === center.woreda_id);
    if (woreda) parts.push(woreda.admin_unit_name_en);
  }
  return parts.join(' > ') || 'Unknown';
}

export function validateLatLon(lat, lon) {
  if (lat < -90 || lat > 90) return { valid: false, error: 'Latitude must be between -90 and 90' };
  if (lon < -180 || lon > 180) return { valid: false, error: 'Longitude must be between -180 and 180' };
  return { valid: true };
}

export function calculateDistance(lat1, lon1, lat2, lon2) {
  const R = 6371000; // Earth radius in meters
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
}

export function getGeofenceBand(distanceM, greenRadius = 50, yellowRadius = 100) {
  if (distanceM <= greenRadius) return 'GREEN';
  if (distanceM <= yellowRadius) return 'YELLOW';
  return 'RED';
}

